#include "libft.h"

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (s1++ && s2++ && i++ <= n)
	{
		if (s1 != s2)
			return (s1 - s2);
	}
	return (0);
}
